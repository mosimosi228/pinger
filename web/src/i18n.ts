const catalogs: Record<Lang, Dict> = { en, ru, es, de, zh };

function detectLang(): Lang {
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
    if (stored && catalogs[stored]) return stored;
  } catch {
    /* ignore */
  }
  return "en";
}

let current: Lang = detectLang();

export function getLang(): Lang {
  return current;
}

export function setLang(lang: Lang): void {
  if (!catalogs[lang]) return;
  current = lang;
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch {
    /* ignore */
  }
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
}

export function t(key: string, vars?: Record<string, string | number>): string {
  return translate(current, key, vars);
}

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const dict = catalogs[lang] || en;
  let text = dict[key] ?? en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

/** Run callback with a temporary language (does not persist). */
export function withLang<T>(lang: Lang, fn: () => T): T {
  const prev = current;
  current = catalogs[lang] ? lang : "en";
  try {
    return fn();
  } finally {
    current = prev;
  }
}

setLang(current);
